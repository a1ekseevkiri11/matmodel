clc; % Очищает командное окно
% Параметры улитки Паскаля
a = 3; % Множитель при cos(φ)
b = 2; % Константа
% Инициализация переменных
N = 1000; % Количество точек
phi = zeros(1, N); % Массив для углов
r = zeros(1, N); % Массив для радиусов
% Генерация углов и вычисление радиусов
for i = 1:N
    phi(i) = (i-1) * 2 * pi / (N-1); % Углы от 0 до 2π
    r(i) = a * cos(phi(i)) + b; % Уравнение улитки Паскаля
end
% Построение графика в полярных координатах
polarplot(phi, r, "k-", 'LineWidth', 2); % Построение графика
% Настройки графика
title('Улитка Паскаля'); % Название графика
grid on; % Включение сетки
