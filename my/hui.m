clc; % Очищает командное окно
clear; % Очищает все переменные

% Параметры окружностей
r = 5; % Радиус обеих окружностей

% Центры окружностей
center1 = [-r, 0]; % Центр первой окружности (слева)
center2 = [r, 0];  % Центр второй окружности (справа)

% Углы для построения окружностей
theta_1 = linspace(0, -1.5*pi, 100); % Полный круг
theta_2 = linspace(0, 1.5*pi, 100); % Полный круг
theta = linspace(0, 1*pi, 100); % Полный круг

% Координаты точек для первой окружности
x1 = r * cos(theta_1) + center1(1);
y1 = r * sin(theta_1) + center1(2);

% Координаты точек для второй окружности
x2 = r * -cos(theta_2) + center2(1);
y2 = r * -sin(theta_2) + center2(2);

% Параметры эллипса (овал)
a = 2 * r; % Горизонтальная полуось эллипса (расстояние между центрами окружностей)
b = 5 * r;     % Вертикальная полуось эллипса, чтобы касаться окружностей

% Центр эллипса (находится между окружностями)
center_ellipse = [0, 5]; % Центр эллипса посередине

% Координаты для эллипса
x_ellipse = a * cos(theta) / 2 + center_ellipse(1); % Горизонтальная полуось
y_ellipse = b * sin(theta) + center_ellipse(2);    % Вертикальная полуось

% Построение графика
figure;
plot(x1, y1, 'b-', 'LineWidth', 4); % Первая окружность
hold on;
plot(x2, y2, 'r-', 'LineWidth', 4); % Вторая окружность
plot(x_ellipse, y_ellipse, 'g-', 'LineWidth', 4); % Эллипс (овал)

% Оформление графика
axis equal;
grid on;
xlabel('x');
ylabel('y');
title('Две окружности и овал, касающийся их в одной точке');
legend('Окружность 1', 'Окружность 2', 'Овал');

hold off;